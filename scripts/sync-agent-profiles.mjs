#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const defaultRoot = path.resolve(scriptDirectory, "..");
const allowedFields = new Set(["name", "description", "sandbox_mode"]);
const allowedSandboxes = new Set(["read-only", "workspace-write"]);

const parseScalar = (value) => {
  const trimmed = value.trim();
  if (!trimmed) return "";
  if (trimmed.startsWith('"')) {
    try { return JSON.parse(trimmed); }
    catch (error) { throw new Error(`invalid quoted frontmatter value: ${error.message}`); }
  }
  return trimmed;
};

export const parseAgentDefinition = (file) => {
  const content = fs.readFileSync(file, "utf8");
  const match = content.match(/^---\n([\s\S]*?)\n---\n([\s\S]+)$/);
  if (!match) throw new Error(`${file}: missing agent frontmatter`);

  const metadata = {};
  for (const line of match[1].split("\n")) {
    if (!line.trim()) continue;
    const separator = line.indexOf(":");
    if (separator < 0) throw new Error(`${file}: invalid frontmatter line: ${line}`);
    const key = line.slice(0, separator).trim();
    if (!allowedFields.has(key)) throw new Error(`${file}: unsupported frontmatter field: ${key}`);
    metadata[key] = parseScalar(line.slice(separator + 1));
  }

  for (const field of allowedFields) {
    if (!metadata[field]) throw new Error(`${file}: missing ${field}`);
  }
  if (!/^[a-z][a-z0-9_]*$/.test(metadata.name)) throw new Error(`${file}: invalid agent name: ${metadata.name}`);
  if (!allowedSandboxes.has(metadata.sandbox_mode)) throw new Error(`${file}: unsupported sandbox_mode: ${metadata.sandbox_mode}`);
  if (metadata.description.length < 40) throw new Error(`${file}: description is too short`);

  const developerInstructions = match[2].trim();
  if (developerInstructions.length < 120) throw new Error(`${file}: developer instructions are too short`);
  return {
    source: path.basename(file),
    file: `${metadata.name}.toml`,
    name: metadata.name,
    description: metadata.description,
    sandboxMode: metadata.sandbox_mode,
    developerInstructions
  };
};

export const loadAgentDefinitions = (root = defaultRoot) => {
  const agentsRoot = path.join(root, "plugins", "codex-agent", "agents");
  if (!fs.existsSync(agentsRoot)) throw new Error(`Agent source directory not found: ${agentsRoot}`);
  const definitions = fs.readdirSync(agentsRoot, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith(".md"))
    .map((entry) => parseAgentDefinition(path.join(agentsRoot, entry.name)))
    .sort((left, right) => left.name.localeCompare(right.name));
  const names = new Set();
  for (const definition of definitions) {
    if (names.has(definition.name)) throw new Error(`Duplicate agent name: ${definition.name}`);
    names.add(definition.name);
  }
  return definitions;
};

export const renderAgentProfilesModule = (definitions) => [
  "// Generated by scripts/sync-agent-profiles.mjs. Do not edit directly.",
  `export const agentProfiles = ${JSON.stringify(definitions, null, 2)};`,
  ""
].join("\n");

export const renderAgentToml = (definition) => [
  `name = ${JSON.stringify(definition.name)}`,
  `description = ${JSON.stringify(definition.description)}`,
  `sandbox_mode = ${JSON.stringify(definition.sandboxMode)}`,
  `developer_instructions = ${JSON.stringify(definition.developerInstructions)}`,
  ""
].join("\n");

export const agentArtifacts = (root = defaultRoot) => {
  const definitions = loadAgentDefinitions(root);
  const artifacts = new Map([
    [path.join(root, "plugins", "codex-agent", "generated", "agent-profiles.mjs"), renderAgentProfilesModule(definitions)]
  ]);
  for (const definition of definitions) {
    artifacts.set(path.join(root, "templates", "project", ".codex", "agents", definition.file), renderAgentToml(definition));
  }
  return { definitions, artifacts };
};

export const syncAgentProfiles = ({ root = defaultRoot, write = false } = {}) => {
  const { definitions, artifacts } = agentArtifacts(root);
  const expectedPaths = new Set(artifacts.keys());
  const templateRoot = path.join(root, "templates", "project", ".codex", "agents");
  const stale = fs.existsSync(templateRoot)
    ? fs.readdirSync(templateRoot)
      .filter((name) => name.endsWith(".toml"))
      .map((name) => path.join(templateRoot, name))
      .filter((file) => !expectedPaths.has(file))
    : [];
  const mismatches = [];

  for (const [file, expected] of artifacts) {
    const actual = fs.existsSync(file) ? fs.readFileSync(file, "utf8") : null;
    if (actual !== expected) mismatches.push(path.relative(root, file));
    if (write && actual !== expected) {
      fs.mkdirSync(path.dirname(file), { recursive: true });
      fs.writeFileSync(file, expected);
    }
  }
  for (const file of stale) {
    mismatches.push(path.relative(root, file));
    if (write) fs.unlinkSync(file);
  }
  return { definitions, mismatches: [...new Set(mismatches)].sort(), written: write };
};

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  const write = process.argv.includes("--write");
  const result = syncAgentProfiles({ write });
  if (!write && result.mismatches.length) {
    for (const file of result.mismatches) process.stderr.write(`out of sync: ${file}\n`);
    process.exitCode = 1;
  } else {
    process.stdout.write(`${write ? "Synchronized" : "Validated"} ${result.definitions.length} agent profiles.\n`);
  }
}
